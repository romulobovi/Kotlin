kotlin
fun main() {
    // Laço for
    for (i in 1..5) {
        println("Número: $i")
    }

    // Laço while
    var j = 1
    while (j <= 5) {
        println("Número: $j")
        j++
    }

    // Laço do-while
    var k = 1
    do {
        println("Número: $k")
        k++
    } while (k <= 5)
}